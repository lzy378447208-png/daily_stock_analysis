"""
===================================
趋势交易分析器 - 基于用户交易理念 & AI 强题材赋能
===================================

交易理念核心原则：
1. 严进策略 - 不追高，追求每笔交易成功率
2. 趋势交易 - MA5>MA10>MA20 多头排列，顺势而为
3. 效率优先 - 关注筹码结构好的股票
4. 买点偏好 - 在 MA5/MA10 附近回踩买入

技术标准：
- 多头排列：MA5 > MA10 > MA20
- 乖离率：(Close - MA5) / MA5 < 5%（不追高）
- 量能形态：缩量回调优先
- AI 赋能：若上个交易日涨停，自动注入题材背景，由 AI 进行连板或题材可持续性深度剖析。
"""

